package com.lxisoft.Domain;


public class Word
{
	private String word;
	private String meaning;

	public void setWord(String name)
	{
		word=name;
	}
	public void setMeaning(String means)
	{
		meaning=means;
	}
	public String getWord()
	{
		return word;
	}
	public String getMeaning()
	{
		return meaning;
	}
}